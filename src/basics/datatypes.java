package basics;

public class datatypes {

	public static void main(String args[])
	{
		float pi=3.14f;
		double db=88.2228;
		char c='c';
		String s="Hello";
		System.out.println("pi is float "+pi);
		System.out.println("DB is double "+db);
		System.out.println("C is char "+c);
		System.out.println("S is String "+s);
}
}